    private void solveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_solveButtonActionPerformed
        redir.redirect(outputText);
        outputText.setText("");
        String textLoad = validator.validateClause(inputText.getText());
        manager.sendCommand(commander.prepareCommand(PrologCommands.SOLVE_NEW_CROSSROAD.getCommand(), new Object[]{textLoad}));
    }//GEN-LAST:event_solveButtonActionPerformed

    private void cleanButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cleanButtonActionPerformed
        inputText.setText("");
        outputText.setText("");
        detailsText.setText("");
        manager.sendCommand(commander.prepareCommand(PrologCommands.CLEAN.getCommand(), (Object) null));
    }//GEN-LAST:event_cleanButtonActionPerformed

